#ifndef SMPL_TRIANGLE_H
#define SMPL_TRIANGLE_H

#include <smpl/spatial.h>

namespace smpl {
namespace geometry {

struct Triangle
{
    Vector3 a;
    Vector3 b;
    Vector3 c;

    Triangle() = default;

    Triangle(const Vector3& a, const Vector3& b, const Vector3& c) :
        a(a), b(b), c(c)
    { }
};

} // namespace geometry
} // namespace smpl

#endif
